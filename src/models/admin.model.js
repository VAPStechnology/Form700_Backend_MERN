import mongoose,{Schema} from "mongoose";



const adminSchema = new Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
});

export const Admin = mongoose.model("Admin", adminSchema)